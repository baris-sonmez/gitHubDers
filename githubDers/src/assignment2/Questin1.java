package assignment2;

public class Questin1 {
	
	public static void main(String[]args) {
		
		//  	Escape Characters
		
		System.out.println("Merhaba\tdunya");         // \t bir tab boslugu ekler
		System.out.println("Merhaba\ndunya\n");       // \n bir satir asagi atlar
		System.out.println("Merhaba\rdunya");         // \r satir basi ekler
		System.out.println("Merhaba dunya\f");        // \f sayfa sonu belirtir
		System.out.println("\"");                     // \" cift tirnak ekler
		System.out.println("\'");                     // \' tek tirnak ekler
		System.out.println("\\");                     // \\ ters slash ekler ekler
	
  
		
		
		
	}

}
